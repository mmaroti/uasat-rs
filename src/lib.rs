extern crate cfg_if;
extern crate wasm_bindgen;

use wasm_bindgen::prelude::*;

cfg_if::cfg_if! {
if #[cfg(feature = "wee_alloc")] {
        extern crate wee_alloc;
        #[global_allocator]
        static ALLOC: wee_alloc::WeeAlloc = wee_alloc::WeeAlloc::INIT;
    }
}

#[wasm_bindgen]
pub fn uasat_test(a: u32, b: u32) -> u32 {
    a + b
}
